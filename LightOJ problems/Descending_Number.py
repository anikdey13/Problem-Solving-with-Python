# প্রতিটি লাইনে মোট 5টি করে সংখ্যা থাকবে এবং প্রতিটি সংখ্যা একটি (space) ক্যারেক্টার দিয়ে আলাদা করা থাকবে।


n = 1000

for i in range(1,1001): 
      if i%5==0:              
          print(n)
          n = n-1
      else:
          print(n,end=' ')
          n = n-1